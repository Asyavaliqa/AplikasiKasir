            conn = Database.getCon();
            pstmt = conn.prepareStatement("UPDATE `user` SET `username` = ?, `password` = ?, `nama_user` = ?, `id_level` = ? WHERE `id_user` = ?");
            pstmt.setString(1, Username.getText());
