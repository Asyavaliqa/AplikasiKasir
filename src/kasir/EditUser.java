    public EditUser(java.awt.Frame parent, boolean modal, int userId) {
        super(parent, modal);
        initComponents();
        this.userId = userId;
        try {
            conn = Database.getCon();
            stmt = conn.createStatement();
            rs = stmt.executeQuery("SELECT * FROM `level`");
            while (rs.next()) {
                Role.addItem(rs.getString("nama_level"));
            }
            rs.close();
            stmt.close();
            
            pstmt = conn.prepareStatement("SELECT * FROM `user` WHERE `id_user` = ? LIMIT 1");
            pstmt.setInt(1, userId);
            rs = pstmt.executeQuery();
            if (rs.next()) {
                Username.setText(rs.getString("username"));
                Fullname.setText(rs.getString("nama_user"));
                Password.setText(rs.getString("password"));
                Role.setSelectedIndex(rs.getInt("id_level")-1);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ManageUser.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

